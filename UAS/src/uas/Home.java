    public List<Clothes> searchFromDatabase(String keyword) {
        List<Clothes> results = new ArrayList<>();

        Connection conn = null;
        PreparedStatement stat = null;
        ResultSet rs = null;
        String query = "SELECT * FROM tbbaju WHERE id LIKE ? OR merk LIKE ? OR type LIKE ? OR color LIKE ? OR size LIKE ?";

        try {
            conn = Conn.getConnection();
            stat = conn.prepareStatement(query);
            stat.setString(1, "%" + keyword + "%");
            stat.setString(2, "%" + keyword + "%");
            stat.setString(3, "%" + keyword + "%");
            stat.setString(4, "%" + keyword + "%");
            stat.setString(5, "%" + keyword + "%");

            // Menjalankan pernyataan SQL
            rs = stat.executeQuery();

            // Mengambil hasil pencarian dan menambahkannya ke dalam daftar hasil
            while (rs.next()) {
                String id = rs.getString("id");
                String merk = rs.getString("merk");
                String type = rs.getString("type");
                String color = rs.getString("color");
                String size = rs.getString("size");
                String amount = rs.getString("amount");

                Clothes clothes = new Clothes(id, merk, type, color, size, amount);
                results.add(clothes);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            // Menutup statement, result set, dan koneksi
            try {
                if (rs != null) {
                    rs.close();
                }
                if (stat != null) {
                    stat.close();
                }
                if (conn != null) {
                    conn.close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }

        return results;
    }

