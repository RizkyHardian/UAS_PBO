    public void showTable() {
        tb.setRowCount(0);
        setTable();
        Connection conn = Conn.getConnection();
        Statement stat = null;
        ResultSet rs = null;
        try {
            stat = conn.createStatement();
            String query = "SELECT * FROM tbbaju";
            rs = stat.executeQuery(query);
            while (rs.next()) {
                String id = rs.getString("id");
                String merk = rs.getString("merk");
                String type = rs.getString("type");
                String color = rs.getString("color");
                String size = rs.getString("size");
                String amount = rs.getString("amount");

                tb.addRow(new Object[]{id, merk, type, color, size, amount});
            }
        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            if (rs != null) {
                try {
                    rs.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
            if (stat != null) {
                try {
                    stat.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
            if (conn != null) {
                try {
                    conn.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
        }
        jTable2.setModel(tb);
    }
